#!/usr/bin/env python3
"""This module contains a function to perform addition."""


def add(a: float, b: float) -> float:
    """Compute the sum of two floating-point numbers.

    Args:
        a (float): The first addend.
        b (float): The second addend.

    Returns:
        float: The sum of `a` and `b`.
    """
    return a + b
