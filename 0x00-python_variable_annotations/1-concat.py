#!/usr/bin/env python3

"""
Concatenates two strings.
"""


def concat(string1: str, string2: str) -> str:
    """
    Concatenates two strings.

    Args:
        string1 (str): The first string.
        string2 (str): The second string.

    Returns:
        str: The concatenated string.
    """
    return string1 + string2
